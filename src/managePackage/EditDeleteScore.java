    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (jTextFieldStudentID.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No Student Selected");
        }
        else {
            // xóa dựa theo Id
            int student_id = Integer.valueOf(jTextFieldStudentID.getText());
            
            sc.insertUpdateDeleteScore('d', student_id, null, 0, null);
            // re-count student from table

            EditDeleteScore.jTableScoreDecr.setModel(new DefaultTableModel(null, new Object[] {"student_id","course_id","score", "description"}));
            sc.fillScoreJtable(EditDeleteScore.jTableScoreDecr, "");
            
        
            // Set all input row to be Empty again
            jTextFieldStudentID.setText("");
            jTextField_Score.setText("");
            cr.fillCourseCombo(jComboBoxCourseID);
            jTextField_Description.setText("");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

