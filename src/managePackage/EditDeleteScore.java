    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        if (verifyText()){
            // get input from course TextField  
            String selected_course = jComboBoxCourseID.getSelectedItem().toString();

            int stdId = Integer.valueOf(jTextFieldStudentID.getText());
            int crsId = cr.getCourseId(selected_course);
            double score = Double.valueOf(jTextField_Score.getText());
            String description =  jTextField_Description.getText();


            sc.insertUpdateDeleteScore('u', stdId, crsId, score, description);
            // update table after modified its
            EditDeleteScore.jTableScoreDecr.setModel(new DefaultTableModel(null, new Object[] {"student_id", "course_id", "student_score", "description"}));
            sc.fillScoreJtable(EditDeleteScore.jTableScoreDecr, "");
            
            showScoreForm.jTableShowAll.setModel(new DefaultTableModel(null, new Object[]{"Student Id","Fist Name","Last Name","Course","Score"}));
            sc.showAllScores(showScoreForm.jTableShowAll, "");

        }
        
    }//GEN-LAST:event_jButtonUpdateActionPerformed

