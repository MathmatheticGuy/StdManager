    private void jTableScoreDecrKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableScoreDecrKeyReleased
        // moving Arrow Key Up and Down with info Appear at Input
        if (evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN){
            int rowIndex = jTableScoreDecr.getSelectedRow();
            jTextFieldStudentID.setText(jTableScoreDecr.getValueAt(rowIndex, 0).toString());
            // jComboBoxCourseID.setValue(jTableScore.getValueAt(rowIndex, 1).toString());
            jTextField_Score.setText(jTableScoreDecr.getValueAt(rowIndex, 2).toString() );
            jTextField_Description.setText(jTableScoreDecr.getValueAt(rowIndex, 3).toString());
        }          
    }//GEN-LAST:event_jTableScoreDecrKeyReleased

