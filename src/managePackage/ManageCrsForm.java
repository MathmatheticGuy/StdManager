    private void jTableCourseKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableCourseKeyReleased
        // Set new Table with new Input each Update
        // While Arrow Key moving Up or Down info Appear at Input
        if (evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN){

            int rowIndex = jTableCourse.getSelectedRow();
            jTextField1_Id.setText(jTableCourse.getValueAt(rowIndex, 0).toString());
            jTextField1_label. setText(jTableCourse.getValueAt(rowIndex, 1).toString());
            jSpinnerHours.setValue(Integer.valueOf(jTableCourse.getValueAt(rowIndex, 2).toString() ));
            
        }        
    }//GEN-LAST:event_jTableCourseKeyReleased


