    public boolean verifyText(){
       if (jTextField1_Id.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Please Enter a valid Value");
           return false;
       }
       else {
           return true;
       }
    }
    
    private void jButtonAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddCourseActionPerformed
        // if not equal to nothing
        if (!jTextField1_label.getText().equals("")){
            // If course doesn't exist then insert
            if (!cr.isCourseExist(jTextField1_label.getText())){
                // get input from course TextField
                String label = jTextField1_label.getText();
                int hours = Integer.valueOf(jSpinnerHours.getValue().toString());

                cr.insertUpdateDeleteCourse('i', null, label, hours);
                
                // update table after modified its
                ManageCrsForm.jTableCourse.setModel(new DefaultTableModel(null, new Object[] {"id","label","hours"}));
                cr.fillCourseJtable(ManageCrsForm.jTableCourse, "");
                MainForm.jLabel2_crsCount.setText(" Course Count : " + Integer.toString(MyFunction.countData("course")));

            }

            // duplicate label
            else{
                JOptionPane.showMessageDialog(null, "Course Already Exist");
            } 
        }
        
        // null input
        else{
            JOptionPane.showMessageDialog(null, "Please Enter Something Darling");
        }

    }//GEN-LAST:event_jButtonAddCourseActionPerformed

    private void jButtonCancel1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancel1ActionPerformed
        if (jTextField1_Id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No Course Selected");
        }
        else {
            // xóa dựa theo Id
            int id = Integer.valueOf(jTextField1_Id.getText());

            cr.insertUpdateDeleteCourse('d', id, null, null);
            // re-count student from table

            ManageCrsForm.jTableCourse.setModel(new DefaultTableModel(null, new Object[] {"id","label","hours"}));
            cr.fillCourseJtable(ManageCrsForm.jTableCourse, "");
            MainForm.jLabel2_crsCount.setText("Course Count: " + Integer.toString(MyFunction.countData("course")));

            // Set all input row to be Empty again
            jTextField1_label.setText("");
            jSpinnerHours.setValue(5);
            jTextField1_Id.setText("");
        }
    }//GEN-LAST:event_jButtonCancel1ActionPerformed
    
   
    
    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        
        if (verifyText()){
            // get input from course TextField  
            int id = Integer.valueOf(jTextField1_Id.getText());
            String label = jTextField1_label.getText();
            int hours = Integer.valueOf(jSpinnerHours.getValue().toString());


            cr.insertUpdateDeleteCourse('u', id, label, hours);
            // update table after modified its
            ManageCrsForm.jTableCourse.setModel(new DefaultTableModel(null, new Object[] {"id","label","hours"}));
            cr.fillCourseJtable(ManageCrsForm.jTableCourse, "");
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jTableCourseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCourseMouseClicked
        // get data from selected row
        int index = jTableCourse.getSelectedRow();
        // get value from row at index ? and row ? to refer TextField
        // toString() because TextField is String type
        jTextField1_Id.setText(jTableCourse.getValueAt(index, 0).toString());
        jTextField1_label. setText(jTableCourse.getValueAt(index, 1).toString());
        jSpinnerHours.setValue(Integer.valueOf(jTableCourse.getValueAt(index, 2).toString() ));
    }//GEN-LAST:event_jTableCourseMouseClicked

